import turtle

# move the cursor to where the top-right corner should be if we're centered
turtle.up()
turtle.goto(-50, 100)

# draw the top of the octogon
turtle.down()
turtle.forward(100)

# draw the right upper side of the octogon
turtle.right(45)
turtle.forward(100)

# draw the right side of the octogon
turtle.right(45)
turtle.forward(100)

# draw the right lower side of the octogon
turtle.right(45)
turtle.forward(100)

# draw the bottom of the octogon
turtle.right(45)
turtle.forward(100)

# draw the left lower side of the octogon
turtle.right(45)
turtle.forward(100)

# draw the left side of the octogon
turtle.right(45)
turtle.forward(100)

# draw the left upper side of the octogon
turtle.right(45)
turtle.forward(100)

